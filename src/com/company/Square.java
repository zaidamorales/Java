package com.company;

public class Square {

    float height, width, area;

    public float getWidth() {
        return this.width;
    }
    public void setWidth(float width) {
        this.width = width;
    }

    public float getArea() {
        return this.area;
    }

    public void setArea(float area) {
        this.area = area;
    }

    public float getHeight() {
        return this.height;
    }

    public void setHeight(float height) {
        this.height = height;
    }
}
